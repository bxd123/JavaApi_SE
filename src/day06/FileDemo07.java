package day06;

import java.io.File;

/**
 * 获取一个目录中的所有子项
 * @author Administrator
 * 
 */
public class FileDemo07 {
	public static void main(String[] args) {
		/*
		 * 	获取当前目录中的所有子项
		 */
		File dir = new File(".");
		/*
		 * boolean isFile()
		 * 判断是否为一个文件
		 * 
		 * boolean isDirectory()
		 * 判断是否为一个目录
		 */
		if(dir.isDirectory()){
			/*
			 * File[] listFiles()
			 * 获取所有子项
			 */
			File[] subs = dir.listFiles();
			for(File sub : subs){
				if(sub.isFile()){
					System.out.print("文件：");
				}else{
					System.out.print("目录：");
				}
				System.out.println(sub);
			}
		}
	}
}
